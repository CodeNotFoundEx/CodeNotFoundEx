    void minimumTotalReturnsSmallestPathSum() {
        List<List<Integer>> triangle = Arrays.asList(
                Arrays.asList(2),
                Arrays.asList(3, 4),
                Arrays.asList(6, 5, 7),
                Arrays.asList(4, 1, 8, 3)
        );

        assertEquals(11, Triangle.minimumTotal(triangle));
    }

    @Test
    void minimumTotalHandlesNegativeValues() {
        List<List<Integer>> triangle = Arrays.asList(
                Arrays.asList(-10),
                Arrays.asList(9, 20),
                Arrays.asList(1, -5, 4)
        );

        assertEquals(-6, Triangle.minimumTotal(triangle));
    }
    
